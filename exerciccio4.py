#Faça um Programa que peça as 4 notas bimestrais e mostre a média.

notaA = float(input('Nota primeiro bimestre: '))
notaB = float(input('Nota Segundo bimestre: '))
notaC = float(input('Nota Terceiro bimestre: '))
notaD = float(input('Nota Quarto bimestre: '))

print("a nota do ano letivo foi: " , (notaA + notaB + notaC + notaD)/4)

